<span style="color:#333; font-weight:bold">Abstract:</span><br>
The global burden of tuberculosis (TB) is enormous, impacting industrialized as well as developing countries. In order to achieve the goal of the 2013 World TB Day Towards Zero TB theme, fast tracking detection of Mycobacterium tuberculosis (MTB)\ and drug resistant strains are critical in breaking the chain of TB transmission. Cases of TB are on the decline in the USA; however, New York City has three times the average USA cases. Although culture remains the gold standard for MTB detection as per the guidelines of the Centers for Disease Control and Prevention, nucleic acid amplification tests (NAAT), namely the Xpert MTB/RIF (Cepheid) or the MTD (Gen-Probe), are highly recommended as critical assays for TB diagnosis. The advantages to the Xpert MTB/RIF are simultaneous MTB detection and resistance to rifampin, which is used as a marker for multi-drug resistance. The Clinical Microbiology Service at Columbia University Medical Center, a major teaching hospital in NYC, has utilized NAATs for rapid detection of MTB in both pulmonary and non-pulmonary acid-fast smear variable specimens. In addition, the automated AutoGenomics BioFilmChip Microarray technology is used to detect MTB resistance to rifampin and isoniazid and to differentiate Mycobacterium bovis from MTB. Gene sequencing using 65 kDa heat shock protein (hsp65) primers is employed for identification to species level of all mycobacteria. New paradigms for the laboratory diagnosis of pulmonary and extrapulmonary TB, particularly acid-fast smear negative cases, are being introduced due to the implementation of these highly sensitive and specific molecular based assays. </div>
